namespace MusicBeeRemoteCore.AndroidRemote.Enumerations
{
    /// <summary>
    /// The shuffle state of the player.
    /// </summary>
    public enum Shuffle
    {
        /// <summary>
        /// The shuffle functionality is disabled.
        /// </summary>
        off,

        /// <summary>
        /// The player has autodj functionality enabled.
        /// </summary>
        autodj,

        /// <summary>
        /// The shuffle functionality is active.
        /// </summary>
        shuffle
    }
}